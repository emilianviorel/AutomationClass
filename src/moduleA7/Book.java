// Class representing a Book
package moduleA7;

public class Book {

    // Instance variables
    double price;
    String title;
    String writer;

    // Method to get the price of the book
    public double getPrice() {
        return price;
    }

    // Method to display information about the book
    public String display() {
        return "Book " + title + " is written by " + writer;
    }
}
